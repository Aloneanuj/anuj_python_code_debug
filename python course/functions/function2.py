def add(x, y):
    sum = x + y
    print("sum = ", sum)

def subtraction(x, y):
    sub = x - y
    print("subtraction = ", sub)


def multiply(x, y):
    mul = x * y
    print("subtraction = ", mul)


def division(x, y):
    div = x / y
    print("subtraction = ", div)


a = int(input("Enter first Integer value : "))
b = int(input("Enter second Integer value : "))
add(a, b)
subtraction(a, b)
multiply(a, b)
division(a, b)
